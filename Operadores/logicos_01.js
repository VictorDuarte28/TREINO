let havemoney = true;
let its_sunny = false;
let carisinthegarage = true;

// Símbolo do E
let resultAND = "#1 - Go to the mall ?\n";
resultAND += havemoney && its_sunny;
console.log(resultAND);

// Símbolo do OU
let resultOR = "#2 - Go to the mall ?\n";
resultOR += carisinthegarage || its_sunny;
console.log(resultOR);

// Símbolo do diferente
console.log(true != false);

// Símbolo do Operador lógico ("não verdadeiro" ou "Não falso")
console.log("Not true: " + !true);
console.log("Not false: " + !false);


